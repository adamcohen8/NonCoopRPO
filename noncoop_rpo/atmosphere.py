EARTH_EQUATORIAL_RADIUS_KM = 6378.137
EARTH_FLATTENING = 0.003352810664747352
EARTH_ROT_RATE_RAD_S = 7.2921159e-5


def geodetic_height_km(r_eci_km: np.ndarray, max_iter: int = 10, tol: float = 1e-5) -> float:
    """
    Compute ellipsoidal geodetic height above an oblate Earth from ECI position.
    Mirrors the iterative logic used in Vallado/Lisowski atmos76.m.
    """
    ae = EARTH_EQUATORIAL_RADIUS_KM
    flat = EARTH_FLATTENING

    two_f_minus_f2 = 2.0 * flat - flat * flat
    one_minus_f2 = (1.0 - flat) ** 2

    r_norm = np.linalg.norm(r_eci_km)
    if r_norm <= 0.0:
        raise ValueError('Position norm must be positive.')

    delta = np.arcsin(r_eci_km[2] / r_norm)
    geo_cn_lat = delta
    old_delta = 1.0
    delta_lat = 10.0
    r_sq = r_norm * r_norm

    height = r_norm - ae
    it = 0
    while abs(old_delta - delta_lat) > tol and it < max_iter:
        old_delta = delta_lat

        denom = 1.0 - two_f_minus_f2 * np.cos(geo_cn_lat) * np.cos(geo_cn_lat)
        rc = ae * np.sqrt((1.0 - two_f_minus_f2) / denom)

        if abs(geo_cn_lat - 0.5 * np.pi) < 1e-6:
            geo_dt_lat = geo_cn_lat
        else:
            geo_dt_lat = np.arctan(np.tan(geo_cn_lat) / one_minus_f2)

        temp = geo_dt_lat - geo_cn_lat
        sin_temp = np.sin(temp)

        height = np.sqrt(r_sq - rc * rc * sin_temp * sin_temp) - rc * np.cos(temp)
        delta_lat = np.arcsin(height * sin_temp / r_norm)
        geo_cn_lat = delta - delta_lat
        it += 1

    return float(height)


def atmos76_density_from_altitude_km(h_km: float) -> float:
    """
    Density from 1976 atmosphere table, with linear interpolation.
    Returns kg/km^3.
    """
    if h_km >= 1000.0:
        return 0.0

    h = max(0.0, float(h_km))
    step = 0.5
    index = int(np.floor(h / step))
    if index >= len(_AD76_KG_PER_KM3) - 1:
        return float(_AD76_KG_PER_KM3[-1])

    h0 = step * index
    frac = (h - h0) / step
    y1 = _AD76_KG_PER_KM3[index]
    y2 = _AD76_KG_PER_KM3[index + 1]
    return float(y1 + frac * (y2 - y1))


def atmos76_density_from_eci(r_eci_km: np.ndarray) -> float:
    """
    Density (kg/km^3) at ECI position using the 1976 atmosphere lookup model.
    """
    h_km = geodetic_height_km(np.asarray(r_eci_km, dtype=float))
    return atmos76_density_from_altitude_km(h_km)
